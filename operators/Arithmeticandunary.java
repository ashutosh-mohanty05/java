package operators;

public class Arithmeticandunary {
    public static void main(String[] args){
        int a=10;
        int b=5;
        int diff=a/b;
        int mul=a*b;
        int mol=a%b;
        int add=a+b;
        int sub=a-b;
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(mol);
        System.out.println(add);
        System.out.println(sub);


        int z=11;
        System.out.println(z++);
        System.out.println(z);
        System.out.println(z--);
        System.out.println(z);
        System.out.println(++z);
        System.out.println(z);
        System.out.println(--z);
        System.out.println(z);
    }
}
